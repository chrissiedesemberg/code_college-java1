    // Coding Challenge 15:  Number Palindrome
    public static boolean isPalindrome(int number) {

        int reverse = 0;
        int recordedNumber = number;

        while (number != 0) {
            int lastDigit = number % 10;
            number = number / 10;
            reverse = reverse * 10;
            reverse += lastDigit;
        }

        if (reverse == recordedNumber) {
            return true;
        } else {
            return false;
        }

    }

