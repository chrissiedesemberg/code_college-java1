    // Coding Exercise 20: Greatest Common Divisor
    public static int getGreatestCommonDivisor(int first, int second) {

        if (first < 10 || second < 10) {
            return -1;
        }

        int maxCommonDivisor = 0;
        int i, j;

        for (i = 1; i < first; i++) {
            for (j = 1; j < second; j++) {
                if (first % i == 0 && second % i == 0 && first % j == 0 && second % i == 0) {
                    maxCommonDivisor = i;
                }
            }
        }
        return maxCommonDivisor;
    }

