    public static int sumOdd(int start, int end) {

        int sum = 0;
        if ((start < 0 || end < 0) || end < start) {
            sum += -1;
            return sum;
        }

        for (int i = start; i <= end; i++) {
            if (isOdd(i) == true) {
                sum += i;
            }
        }
        return sum;
    }

