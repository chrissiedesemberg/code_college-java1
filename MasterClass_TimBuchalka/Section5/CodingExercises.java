    // Coding Exercise 26: Diagonal Star
    public static void printSquareStar(int number) {

        if (number < 5) {
            System.out.println("Invalid Value");
            return;
        }

        for (int row = 1; row <= number; row++) {
            for (int col = 1; col <= number; col++) {
                if (row == 1 || row == number || col == 1 || col == number) {
                    System.out.print("*");
                } else if (row == col) {
                    System.out.print("*");
                } else if (col == (number - row + 1)) {
                    System.out.print("*");
                } else {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }

    }

    // Coding Exercise 25: Largest Prime
    public static int getLargestPrime(int number) {
        if (number >= 2) {
            for (int i = 2; i < number; i++) {
                if (number % i == 0) {
                    number /= i;
                    i--;
                }
            }
            return number;
        }
        return -1;
    }

    //Coding Exercise 24: Flour Pack Problem
    public static boolean canPack(int bigCount, int smallCount, int goal) {

        if (bigCount < 0 || smallCount < 0 || goal < 0) {
            return false;
        }

        int bigBagWeight = 5;
        int smallBagWeight = 1;

        int bigCountWeight = bigCount * bigBagWeight;
        int smallCountWeight = smallCount * smallBagWeight;

        int totalWeight = bigCountWeight + smallCountWeight;

        boolean canDo = true;

        if (totalWeight == goal) {
            canDo = true;
        } else if (totalWeight < goal) {
            canDo = false;
        } else if (totalWeight > goal) {
            //true statements
            if (goal % bigBagWeight == 0 && smallCountWeight > 0) {
                canDo = true;
            } else if (goal % bigBagWeight > 1 && smallCountWeight > 0 && ((goal - bigBagWeight) + smallCountWeight) > goal) {
                canDo = true;
            } else if (bigCountWeight <= goal && smallCountWeight > 0) {
                canDo = true;
            } else if (bigCountWeight == 0 && smallCountWeight > goal) {
                canDo = true;
            } else {
                canDo = false;
            }
        }

        return canDo;
    }

