    public static int getDigitCount(int number) {

        if (number < 0) {
            return -1;
        }

        int digits = 0;
        String numberAsString = Integer.toString(number);

        for (int i = 0; i < numberAsString.length(); i++) {
            digits += 1;
        }
        return digits;
    }

    public static int reverse(int number) {

        String reversedNumber = "";
        int originalNumber = number;
        int reversedNumberToInt = 0;
        int lastDigit;

        //make number positive
        if (number < 0) {
            number *= -1;
        }

        //make number into string
        String numberToString = Integer.toString(number);

        if (number >= 10) {
            for (int i = 0; i < numberToString.length(); i++) {
                lastDigit = number % 10;
                number = number / 10;
                reversedNumber += Integer.toString(lastDigit);
            }
            if (number != 0) {
                reversedNumber += Integer.toString(number);
            }
            reversedNumberToInt = Integer.valueOf(reversedNumber);
        } else {
            if (originalNumber < 0) {
                reversedNumberToInt = originalNumber * -1;
            } else {
                reversedNumberToInt = originalNumber;
            }
        }

        if (originalNumber < 0) {
            reversedNumberToInt *= -1;
        }

        return reversedNumberToInt;
    }

