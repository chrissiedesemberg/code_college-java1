public class Example3 {
    public static void main(String[] args) {
        int v;
        double x;
    
        v = 10;
        x = 10.0;
        
        System.out.println("Value of v: " + v);
        System.out.println("Value of x: " + x);
        
        v = v / 4;
        x = x / 4;
        
        System.out.println("\t Value of v divided by 4: " + v);
        System.out.println("\t Value of x divided by 4: " + x);
        
    
    }
}
