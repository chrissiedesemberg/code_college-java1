public class CharArithDemo {
    public static void main(String[] args) {
        char ch;
        
        ch = 'X';
        System.out.println("ch contains " + ch);
        
        ch++;
        System.out.println("ch is now " + ch);
        
        ch = 90;
        System.out.println("ch is now the value 90 which is: " + ch);
    }
}
